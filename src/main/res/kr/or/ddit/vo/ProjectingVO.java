package kr.or.ddit.vo;

public class ProjectingVO {
	private String rnum;
	private String pr_num;
	private String pr_title;
	private String client;
	private String cons_payment;
	private String cons_date;
	private String pr_start;
	public String getRnum() {
		return rnum;
	}
	public void setRnum(String rnum) {
		this.rnum = rnum;
	}
	public String getPr_num() {
		return pr_num;
	}
	public void setPr_num(String pr_num) {
		this.pr_num = pr_num;
	}
	public String getPr_title() {
		return pr_title;
	}
	public void setPr_title(String pr_title) {
		this.pr_title = pr_title;
	}
	public String getClient() {
		return client;
	}
	public void setClient(String client) {
		this.client = client;
	}
	public String getCons_payment() {
		return cons_payment;
	}
	public void setCons_payment(String cons_payment) {
		this.cons_payment = cons_payment;
	}
	public String getCons_date() {
		return cons_date;
	}
	public void setCons_date(String cons_date) {
		this.cons_date = cons_date;
	}
	public String getPr_start() {
		return pr_start;
	}
	public void setPr_start(String pr_start) {
		this.pr_start = pr_start;
	} 
}
