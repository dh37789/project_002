package kr.or.ddit.vo;

public class Alarm_messageVO {
	private String armsg_num;
	private String pr_num;
	private String mem_id;
	private String armsg_content;
	private String armsg_status;
	
	public String getArmsg_num() {
		return armsg_num;
	}
	public void setArmsg_num(String armsg_num) {
		this.armsg_num = armsg_num;
	}
	public String getPr_num() {
		return pr_num;
	}
	public void setPr_num(String pr_num) {
		this.pr_num = pr_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getArmsg_content() {
		return armsg_content;
	}
	public void setArmsg_content(String armsg_content) {
		this.armsg_content = armsg_content;
	}
	public String getArmsg_status() {
		return armsg_status;
	}
	public void setArmsg_status(String armsg_status) {
		this.armsg_status = armsg_status;
	}
}
