package kr.or.ddit.vo;

public class LicenseVO {
	private String li_num;
	private String mem_id;
	private String li_name;
	private String li_organization;
	private String li_date;
	private String li_status;
	
	public String getli_num() {
		return li_num;
	}
	public void setli_num(String li_num) {
		this.li_num = li_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getli_name() {
		return li_name;
	}
	public void setli_name(String li_name) {
		this.li_name = li_name;
	}
	public String getli_organization() {
		return li_organization;
	}
	public void setli_organization(String li_organization) {
		this.li_organization = li_organization;
	}
	public String getli_date() {
		return li_date;
	}
	public void setli_date(String li_date) {
		this.li_date = li_date;
	}
	public String getli_status() {
		return li_status;
	}
	public void setli_status(String li_status) {
		this.li_status = li_status;
	}
	
	

}
