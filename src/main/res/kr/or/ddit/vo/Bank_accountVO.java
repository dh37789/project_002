package kr.or.ddit.vo;

public class Bank_accountVO {
	private String ac_num;
	private String ac_account;
	private String ac_mem;
	private String ac_bank;
	private String ac_name;
	
	public String getAc_num() {
		return ac_num;
	}
	public void setAc_num(String ac_num) {
		this.ac_num = ac_num;
	}
	public String getAc_account() {
		return ac_account;
	}
	public void setAc_account(String ac_account) {
		this.ac_account = ac_account;
	}
	public String getAc_mem() {
		return ac_mem;
	}
	public void setAc_mem(String ac_mem) {
		this.ac_mem = ac_mem;
	}
	public String getAc_bank() {
		return ac_bank;
	}
	public void setAc_bank(String ac_bank) {
		this.ac_bank = ac_bank;
	}
	public String getAc_name() {
		return ac_name;
	}
	public void setAc_name(String ac_name) {
		this.ac_name = ac_name;
	}
}
