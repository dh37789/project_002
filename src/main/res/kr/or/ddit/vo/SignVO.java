package kr.or.ddit.vo;

public class SignVO {
	private String sign_num;
	private String mem_id;
	private String sign_save_name;
	private String sign_status;
	private String sign_date;
	private String sign_type;
	
	public String getSign_num() {
		return sign_num;
	}
	public void setSign_num(String sign_num) {
		this.sign_num = sign_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getSign_save_name() {
		return sign_save_name;
	}
	public void setSign_save_name(String sign_save_name) {
		this.sign_save_name = sign_save_name;
	}
	public String getSign_status() {
		return sign_status;
	}
	public void setSign_status(String sign_status) {
		this.sign_status = sign_status;
	}
	public String getSign_date() {
		return sign_date;
	}
	public void setSign_date(String sign_date) {
		this.sign_date = sign_date;
	}
	public String getSign_type() {
		return sign_type;
	}
	public void setSign_type(String sign_type) {
		this.sign_type = sign_type;
	}
}
