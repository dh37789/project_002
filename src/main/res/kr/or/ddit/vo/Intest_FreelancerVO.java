package kr.or.ddit.vo;

public class Intest_FreelancerVO {
	private String infr_num;
	private String infr_client;
	private String infr_free;
	
	public String getInfr_num() {
		return infr_num;
	}
	public void setInfr_num(String infr_num) {
		this.infr_num = infr_num;
	}
	public String getInfr_client() {
		return infr_client;
	}
	public void setInfr_client(String infr_client) {
		this.infr_client = infr_client;
	}
	public String getInfr_free() {
		return infr_free;
	}
	public void setInfr_free(String infr_free) {
		this.infr_free = infr_free;
	}
	
}
