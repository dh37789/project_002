package kr.or.ddit.vo;

public class SchduleVO {
	private String sc_num;
	private String mem_id;
	private String sc_start;
	private String sc_end;
	private String sc_content;
	private String sc_status;
	
	public String getSc_num() {
		return sc_num;
	}
	public void setSc_num(String sc_num) {
		this.sc_num = sc_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getSc_start() {
		return sc_start;
	}
	public void setSc_start(String sc_start) {
		this.sc_start = sc_start;
	}
	public String getSc_end() {
		return sc_end;
	}
	public void setSc_end(String sc_end) {
		this.sc_end = sc_end;
	}
	public String getSc_content() {
		return sc_content;
	}
	public void setSc_content(String sc_content) {
		this.sc_content = sc_content;
	}
	public String getSc_status() {
		return sc_status;
	}
	public void setSc_status(String sc_status) {
		this.sc_status = sc_status;
	}
}
