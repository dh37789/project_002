package kr.or.ddit.vo;

public class ChatVO {
	private String chat_num;
	private String mem_id;
	private String pr_num;
	private String chat_content;
	private String chat_date;
	private String chat_status;
	private String chat_ip;
	
	public String getChat_num() {
		return chat_num;
	}
	public void setChat_num(String chat_num) {
		this.chat_num = chat_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getPr_num() {
		return pr_num;
	}
	public void setPr_num(String pr_num) {
		this.pr_num = pr_num;
	}
	public String getChat_content() {
		return chat_content;
	}
	public void setChat_content(String chat_content) {
		this.chat_content = chat_content;
	}
	public String getChat_date() {
		return chat_date;
	}
	public void setChat_date(String chat_date) {
		this.chat_date = chat_date;
	}
	public String getChat_status() {
		return chat_status;
	}
	public void setChat_status(String chat_status) {
		this.chat_status = chat_status;
	}
	public String getChat_ip() {
		return chat_ip;
	}
	public void setChat_ip(String chat_ip) {
		this.chat_ip = chat_ip;
	}
}
