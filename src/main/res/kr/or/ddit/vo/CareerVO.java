package kr.or.ddit.vo;

public class CareerVO {
	private String ca_num;
	private String mem_id;
	private String ca_company;
	private String ca_in;
	private String ca_out;
	private String ca_department;
	private String ca_position;
	
	
	public String getCa_num() {
		return ca_num;
	}
	public void setCa_num(String ca_num) {
		this.ca_num = ca_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getCa_company() {
		return ca_company;
	}
	public void setCa_company(String ca_company) {
		this.ca_company = ca_company;
	}
	public String getCa_in() {
		return ca_in;
	}
	public void setCa_in(String ca_in) {
		this.ca_in = ca_in;
	}
	public String getCa_out() {
		return ca_out;
	}
	public void setCa_out(String ca_out) {
		this.ca_out = ca_out;
	}
	public String getCa_department() {
		return ca_department;
	}
	public void setCa_department(String ca_department) {
		this.ca_department = ca_department;
	}
	public String getCa_position() {
		return ca_position;
	}
	public void setCa_position(String ca_position) {
		this.ca_position = ca_position;
	}
}
