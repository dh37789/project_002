package kr.or.ddit.vo;

public class TechniqueVO {
	private String tech_num;
	private String mem_id;
	private String tech_type;
	private String tech_fluent;
	private String tech_period;
	private String tech_status;
	private String tech_repre;
	
	public String getTech_num() {
		return tech_num;
	}
	public void setTech_num(String tech_num) {
		this.tech_num = tech_num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getTech_type() {
		return tech_type;
	}
	public void setTech_type(String tech_type) {
		this.tech_type = tech_type;
	}
	public String getTech_fluent() {
		return tech_fluent;
	}
	public void setTech_fluent(String tech_fluent) {
		this.tech_fluent = tech_fluent;
	}
	public String getTech_period() {
		return tech_period;
	}
	public void setTech_period(String tech_period) {
		this.tech_period = tech_period;
	}
	public String getTech_status() {
		return tech_status;
	}
	public void setTech_status(String tech_status) {
		this.tech_status = tech_status;
	}
	public String getTech_repre() {
		return tech_repre;
	}
	public void setTech_repre(String tech_repre) {
		this.tech_repre = tech_repre;
	}
}
